--VALOR ABSOLUTO DE UN NUMERO
print("El valor absoluto es: ",math.abs(-6))

--RAIZ CUADRADA DE UN NUMERO
print("La raiz es: ",math.sqrt(64))

--VALOR DEL NUMERO PI
print("El numero PI es: ",math.pi)

--MINIMO Y MAXIMO ENTRE DOS NUMEROS
print("El mas pequeno entre este rango de numeros es: ",math.min(345,1004))
print("El mas grande entre este rango de numeros es: ",math.max(345,1004))  

--REDONDEAR HACIA EL ENTERO SUPERIOR
print("Redondeo de el siguiente numero maximo es: ",math.ceil(23.45))
print("Redondeo de el siguiente numero minimo es: ",math.floor(23.45))
print("Redondeo de el siguiente numero mas sercano mayor a 50 decimales es: ",math.round(23.45))

--NUMERO ALEATORIOS
print("Generar numero aleatorios Rando 0 a 10 es: ",math.random(0,10))

--POR DEFECTO ESTA FUNCION SI NO TIENE PARAMETROS ME DA UN RANGO DEL 0 AL 1
print("Sin parametros el valor es: ",math.round(math.random()))